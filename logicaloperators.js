//Logical Operators
var number1 = 70, number2 = 21;

//Logical AND - if both the conditionals are true then True otherwise False
if(number1%10 && number2%10){
    console.log("The number is divisible by 10");
}

//Logical OR - if both the conditions false then False otherwise True
if(number1%10 || number2%10){
    console.log("The number is divisible by 10");
}


